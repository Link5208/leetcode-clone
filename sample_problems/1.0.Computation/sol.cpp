#include <iostream>
using namespace std;

int main() {
    int a, b;
    cin >> a >> b;

    // Output results
    cout << a + b << endl;        // Sum
    cout << a - b << endl;        // Difference
    cout << (long long)a * b << endl;  // Product
    cout << a / b << endl;        // Integer Division

    return 0;
}

